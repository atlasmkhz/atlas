  // 의도로 보고 검색을 자동 해제한다. clearSearch()는 카메라를
  // INITIAL_MAP_VIEW로 되돌리지만, 아래 syncToYear(y)가 곧바로 이번
  // 입력값(y) 기준의 평소 화면을 다시 그리므로 결과적으로 사용자가 옮긴
  // 슬라이더 위치가 그대로 반영된다.
